package Cycle;

public class Cycle {
    public String model;
    public int make;
    public int numberOfWheels;

    public Cycle() {
    }
    
    public Cycle(String model, int make, int numberOfWheels) {
        this.model = model;
        this.make = make;
        this.numberOfWheels = numberOfWheels;
    }

    
}
